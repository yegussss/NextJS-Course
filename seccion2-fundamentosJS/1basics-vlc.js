var edad = 10;
var edad = 20;
/*
var se puede volver a declarar.
Al cambiarse dentro de una condicional se cambia globalmente.
*/
console.log(edad);

let Edad = 10;
Edad = 20;
/*
let no se puede volver a declarar, pero se puede volver a asignar.
Puede ser cambiada dentro de una condicional sin ser cambiada
de forma global.
*/
console.log(Edad);
